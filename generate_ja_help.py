def to_rtf_unicode(text):
    """Convert a string to RTF with \\uc0\\uXXXX escapes for non-ASCII characters."""
    result = []
    for ch in text:
        code = ord(ch)
        if code < 128:
            if ch in ('{', '}'):
                result.append('\\' + ch)
            else:
                result.append(ch)
        else:
            if code > 32767:
                code = code - 65536
            result.append(f'\\uc0\\u{code} ')
    return ''.join(result)


