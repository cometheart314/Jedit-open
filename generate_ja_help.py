def u(text):
    """Convert Japanese text to RTF unicode."""
    return to_rtf_unicode(text)


def section(num, title):
    return f"{PARD}\n\\fs2 \\cf0 {BN}{PARD}\n{F5B}\\fs32 \\cf0 {num}. {u(title)}\n{F0}\\b0\\fs24 {BN}"


def subsection(title):
    return f"{PARD}\n{F5B}\\fs28 \\cf0 {u(title)}\n{F0}\\b0\\fs24 {BN}"


def h3(title):
    return f"{PARD}\n{F5B} \\cf0 {u(title)}\n{F0}\\b0 {BN}"


def body(text):
    return f"{PARD}{F4} {u(text)}{F0}{BN}"


def bodyc(text):
    return f"{F4} {u(text)}{F0}{BN}"


def bstart():
    return PARD_BULLET


def b(text):
    return f"\\cf0 \\'95 {F4} {u(text)}{F0}{BN}"


def b1(text):
    return f"\\cf0 \\'95 {F4} {u(text)}{F0}{BN}"


def sk(name, keys):
    """Shortcut item."""
    return f"\n{F5B}\\fs22 \\cf0 {u(name)}\n{F0}\\b0 {keys}{BN}"


